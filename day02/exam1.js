// 변수란? 하나의 자료를 저장하는 메모리(저장 단위) 공간
// 같은파일내 변수명은 중복이 x.
// 1. let     2. 변수/상자/공간 이름 = 변수명 = 카멜표기법(권장) mycar->myCar     
// 3. 대입연산자를 이용해 초기값 대입
