let 이름상자2 = 100   // 서로 다른 변수명으로 선언, *초기값이 있다*

// 변수의 호출 : 선언된 변수명을 작성
이름상자2                       // 변수명 작성해 변수값 호출
console.log(이름상자2)          // 변수명 작성해 변수값 호출하고 console에 log(출력)

// 변수의 값 수정 : 누구를 어떻게, 변수명 = 새로운값
이름상자2 = 200
console.log(이름상자2)

